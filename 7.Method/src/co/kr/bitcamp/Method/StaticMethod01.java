package co.kr.bitcamp.Method;

/*
 * 메서드 정의와 기본적인 자바의 메서드를 작성해보시오
 * 
 * 클래스명, StaticMethod명();
 */
public class StaticMethod01 {
    public static void main(String[] args) {
    
        helloWorld();                   // 메서드 호출
        
    }

    public static void helloWorld() {
        System.out.println("Hello world!");
    }
    
}
